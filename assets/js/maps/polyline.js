  function laundry() {
    var cek = document.getElementById('laundry').checked;
  
    if (cek == true) {
      addMarkerLaundry();
    } else {
      removeMarkerLaundry();
    }
  }
  
  function addMarkerLaundry() {
    for (var i = 0; i < laundryArea.length; i++) {
      var dataLaundry = laundryArea[i];
      var icon = {
        url: domain+'assets/images/kategori/icon/dashboard_laundry.png', // url
        scaledSize: new google.maps.Size(40, 60), // scaled size
      };
      marker = new google.maps.Marker({
        map: map,
        icon: icon,
        position: {lat: dataLaundry.lat, lng: dataLaundry.lng}
      });
      laundrymarkers.push(marker);
  
      var j = laundrymarkers.length - 1;
  
      var contentString = "<div id='"+dataLaundry.id+"' style='width:200px;'></div>";
  
      infowindowlaundry[j] = new google.maps.InfoWindow({
          content: contentString  
      });
  
      google.maps.event.addListener(laundrymarkers[j],'click', (function(marker_window,data_window,info_window){ 
          return function() {
              info_window.open(map,marker_window);
              $('#'+data_window).load(domain+'Administrator/maps/info_windowSOS/'+data_window);
          };
      })(laundrymarkers[j],dataLaundry.id,infowindowlaundry[j]));
    }
  }
  
  function removeMarkerLaundry() {
    for (var i = 0; i < laundrymarkers.length; i++) {
      laundrymarkers[i].setMap(null);
    }
    laundrymarkers = [];
  }
  
