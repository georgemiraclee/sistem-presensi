  function addMarker(idKategori) { 
    for (var i = 0; i < areaKeluhan.length; i++) {
      var dataPhoto = areaKeluhan[i];
      if (dataPhoto.kategori == idKategori) {
        var icon = {
          url: domain+'assets/images/kategori/icon/'+dataPhoto.icon, // url
          scaledSize: new google.maps.Size(40, 60), // scaled size
        };
        var marker = new google.maps.Marker({
          position: {lat: dataPhoto.lat, lng: dataPhoto.lon},
          map: map,
          icon: icon,
          title: 'Hello World!',
          kategori: dataPhoto.kategori,
          lat: dataPhoto.lat,
          lon: dataPhoto.lon,
          id: dataPhoto.id
        });
        markers.push(marker);
        var j = markers.length - 1;
  
        var contentString = "<div id='"+dataPhoto.id+"' style='width:400px;'></div>";
  
        infowindow[j] = new google.maps.InfoWindow({
            content: contentString  
        });
  
        google.maps.event.addListener(markers[j],'click', (function(marker_window,data_window,info_window){ 
            return function() {
                info_window.open(map,marker_window);
                $('#'+data_window).load(domain+'Administrator/maps/info_window/'+data_window);
            };
        })(markers[j],dataPhoto.id,infowindow[j]));
      }
    }
  
    // markerCluster.clearMarkers();
  
    // markerCluster = new MarkerClusterer(map, markers,{imagePath: 'https://developers.google.com/maps/documentation/javascript/examples/markerclusterer/m'});
  }
  
